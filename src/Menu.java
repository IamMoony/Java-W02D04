public class Menu {

    String name;
    int portionVolume;
    int price;
    String mainDish;
    String desert;
    String soup;


    public Menu(String name, int price, int portionVolume) {
        this.name = name;
        this.portionVolume = portionVolume;
        this.price = price;
        this.mainDish = mainDish;
        this.desert = desert;
        this.soup = soup;
    }
}
